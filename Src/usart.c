//串口接收空闲中断初始化
void UART_IT_IDLE_Init(UART_HandleTypeDef *huart, uint8_t *pData, uint16_t Size)
{
		__HAL_UART_CLEAR_FLAG(huart,UART_FLAG_IDLE);               	  //清除空闲中断标志
		HAL_Delay(2);
		__HAL_UART_CLEAR_FLAG(huart,UART_FLAG_TC);                 	  //清除发送标志

		HAL_UART_Receive_DMA(huart, pData, Size);  					  //开启DMA接收
		__HAL_UART_ENABLE_IT(huart, UART_IT_IDLE);                 	  //使能空闲中断

		__HAL_UART_CLEAR_FLAG(huart,UART_FLAG_IDLE);             	  //清除空闲中断标志


//（以上代码功能：将串口接收到的数据通过DMA传递给pData，然后产生串口空闲中断，在中断中做进一步处理）
}

//串口接收空闲中断回调函数
void UARTReceive_IDLECallback(UART_HandleTypeDef *huart, UART_RECEIVETYPE *Uart_RX, uint16_t Size)
{

		if((__HAL_UART_GET_FLAG(huart,UART_FLAG_IDLE) != RESET))       //判断是否产生空闲中断
		{
			__HAL_UART_CLEAR_IDLEFLAG(huart);                          //清除空闲中断标志
			HAL_UART_DMAStop(huart);                                   //停用DMA

			//CNDTR为DMA通道接收数据的计数器（注意是一个递减计数器，所以需要将DMA的缓存区的总长度减去该计数器的值才是DMA通道接收数据的长度）
			Uart_RX->rx_len = Size - huart->hdmarx->Instance->CNDTR;   //复位DMA指针

			Uart_RX->receive_flag = 1;                                 //置位串口接收标志位


			if(huart->Instance == USART1 || huart->Instance == USART2 || huart->Instance == USART3)  //串口4等
			{
				HAL_UART_Receive_DMA(huart, Uart_RX->RX_Buf, Size);
			}
		}
}


