void UART2_SendData(uint8_t *pdata, uint16_t Length)
{  
        uint32_t i = 0;		
		for (i = 0; i < Length; i++)
		{
			UART2_SendByte(pdata[i]);
		}
}  

void UART2_SendByte(uint8_t data)
{
        while((USART2->SR&UART_FLAG_TXE)!=UART_FLAG_TXE);             //等待发送寄存器为空
        USART2->DR = data;
}

void UART2_SendString(char *str)
{
        while((*str)!=0)
        {
                while((USART2->SR&UART_FLAG_TXE)!=UART_FLAG_TXE);    //等待发送寄存器为空
                USART2->DR = *str++;       
        }
}


